import natsort from '../../src'

describe('ip addresses:', () => {
  it('ipv4', () => {
    const arr1 = [
      '192.168.0.100',
      '192.168.0.1',
      '192.168.1.1',
      '192.168.0.250',
      '192.168.1.123',
      '10.0.0.2',
      '10.0.0.1'
    ]
    const arr2 = [
      '10.0.0.1',
      '10.0.0.2',
      '192.168.0.1',
      '192.168.0.100',
      '192.168.0.250',
      '192.168.1.1',
      '192.168.1.123'
    ]
    expect(arr1.sort(natsort())).toEqual(arr2)
  })
})
